def _export_record(
    export_id: str,
    kind: str,
    path: Path,
    expression: str,
    status_rows: dict[str, dict[str, str]],
    source: str,
) -> dict[str, Any]:
    row = status_rows.get(export_id, {})
    exists = path.exists() and path.stat().st_size > 0
    status = row.get("status") or ("created" if exists else "missing")
    if status == "created" and not exists:
        status = "missing"
    return {
        "id": export_id,
        "kind": kind,
        "path": str(path),
        "status": status,
        "created": exists,
        "expression": expression,
        "source": source,
        "message": row.get("message", ""),
    }


def _read_export_status(path: Path) -> dict[str, dict[str, str]]:
    if not path.exists():
        return {}
    with path.open("r", encoding="utf-8", newline="") as file:
        rows = csv.DictReader(file, delimiter="\t")
        return {str(row.get("id") or ""): {key: str(value or "") for key, value in row.items()} for row in rows}


def _parse_max_temperature_csv(path: Path) -> dict[str, Any]:
    if not path.exists():
        return {}
    with path.open("r", encoding="utf-8", newline="") as file:
        rows = csv.DictReader(file)
        for row in rows:
            metric = str(row.get("metric") or "")
            if metric != "max_temperature_C":
                continue
            try:
                return {
                    "max_temperature_C": float(str(row.get("value") or "")),
                    "max_temperature_unit": str(row.get("unit") or "degC"),
                    "max_temperature_expression": str(row.get("expression") or "T"),
                }
            except ValueError:
                return {}
    return {}


def _derive_max_temperature_from_table(table_path: Path, metric_path: Path) -> dict[str, Any]:
    if not table_path.exists():
        return {}
    with table_path.open("r", encoding="utf-8", newline="") as file:
        reader = csv.reader(file)
        header: list[str] | None = None
        temperature_index: int | None = None
        unit = "degC"
        max_value: float | None = None
        for row in reader:
            if not row:
                continue
            if row[0].startswith("%"):
                candidate = [row[0].lstrip("%").strip(), *[cell.strip() for cell in row[1:]]]
                candidate_index, candidate_unit = _temperature_column(candidate)
                if candidate_index is not None:
                    header = candidate
                    temperature_index = candidate_index
                    unit = candidate_unit
                continue
            if header is None:
                header = [cell.strip() for cell in row]
                temperature_index, unit = _temperature_column(header)
                continue
            if temperature_index is None or temperature_index >= len(row):
                continue
            try:
                value = float(row[temperature_index])
            except ValueError:
                continue
            max_value = value if max_value is None else max(max_value, value)
    if max_value is None:
        return {}
    max_temperature_c = max_value - 273.15 if unit.lower() in {"k", "kelvin"} else max_value
    with metric_path.open("w", encoding="utf-8", newline="") as file:
        writer = csv.writer(file)
        writer.writerow(["metric", "value", "unit", "expression", "source"])
        writer.writerow(["max_temperature_C", max_temperature_c, "degC", "T", str(table_path)])
    return {
        "max_temperature_C": max_temperature_c,
        "max_temperature_unit": "degC",
        "max_temperature_expression": "T",
        "max_temperature_source": "temperature_table_csv",
    }


def _temperature_column(header: list[str]) -> tuple[int | None, str]:
    for index, cell in enumerate(header):
        normalized = cell.strip().lower()
        if normalized == "t" or normalized.startswith("t ") or "temperature" in normalized:
            return index, _unit_from_header(cell)
    return None, "degC"


def _unit_from_header(cell: str) -> str:
    begin = cell.find("(")
    end = cell.find(")", begin + 1)
    if begin >= 0 and end > begin:
        return cell[begin + 1 : end].strip() or "degC"
    return "degC"


def _java_string(value: str) -> str:
    escaped = value.replace("\\", "\\\\").replace('"', '\\"').replace("\r", "\\r").replace("\n", "\\n")
    return f'"{escaped}"'


def _write_process_log(path: Path, stdout: str | None, stderr: str | None) -> None:
    text = "\n".join(item.strip() for item in (stdout or "", stderr or "") if item and item.strip())
    path.write_text((text + "\n") if text else "", encoding="utf-8")


def _read_text_lossy(path: Path) -> str:
    if not path.exists():
        return ""
    for encoding in ("utf-8", "gb18030", "utf-16"):
        try:
            return path.read_text(encoding=encoding)
        except UnicodeError:
            continue
    return path.read_bytes().decode("utf-8", errors="replace")


